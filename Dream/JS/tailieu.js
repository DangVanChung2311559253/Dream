function showTailieu() {
    var x = document.getElementById('showtailieu');
    x.style.display = "block"; // Hiện phần tài liệu khi di chuột qua
}

function hideTailieu() {
    var x = document.getElementById('showtailieu');
    x.style.display = "none"; // Ẩn phần tài liệu khi chuột rời khỏi khung
}

